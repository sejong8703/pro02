package com.sejongfood.dto;

public class Cart {
	private String cnum;
	private String pcode;
	private int bpcs;
	private String id;
	public String getCnum() {
		return cnum;
	}
	public void setCnum(String cnum) {
		this.cnum = cnum;
	}
	public String getPcode() {
		return pcode;
	}
	public void setPcode(String pcode) {
		this.pcode = pcode;
	}
	public int getBpcs() {
		return bpcs;
	}
	public void setBpcs(int bpcs) {
		this.bpcs = bpcs;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	

}
